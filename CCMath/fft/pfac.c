int pfac(int n,int *kk,int fe) { 
  int num,j,k,dc=1;
  if(fe=='e') { 
    n-=(n%2); 
    dc=2;
  }
  for(;;n-=dc) { 
    num = n; 
    j = k = 0;
    while(j<31){
      if(num % kpf[k] != 0) { 
	if(k==(NO_PRIMES-1)) 
	  break; 
	++k;
      } else { 
	kk[++j]=kpf[k]; 
	num=num/kpf[k];
	if(num==1) { 
	  kk[0]=j; 
	  return n; 
	} 
      }
    }
  }
}


